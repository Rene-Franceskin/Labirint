function captureLabirintSegments(){
  const segments=[];
  const seen=new Set();
  let currentPoint=null;
  const originalBeginPath=ctx.beginPath.bind(ctx);
  const originalMoveTo=ctx.moveTo.bind(ctx);
  const originalLineTo=ctx.lineTo.bind(ctx);
  const originalFill=ctx.fill.bind(ctx);
  const originalStroke=ctx.stroke.bind(ctx);
  const originalSave=ctx.save.bind(ctx);
  ctx.beginPath=()=>{};
  ctx.moveTo=(x,y)=>{currentPoint={x,y};};
  ctx.lineTo=(x,y)=>{
    if(currentPoint){
      const key=`${currentPoint.x},${currentPoint.y}->${x},${y}`;
      if(!seen.has(key)){
        seen.add(key);
        segments.push({x1:currentPoint.x,y1:currentPoint.y,x2:x,y2:y});
      }
    }
    currentPoint={x,y};
  };
  ctx.fill=()=>{};
  ctx.stroke=()=>{};
  ctx.save=()=>{};
  try{
    labirint();
  }finally{
    ctx.beginPath=originalBeginPath;
    ctx.moveTo=originalMoveTo;
    ctx.lineTo=originalLineTo;
    ctx.fill=originalFill;
    ctx.stroke=originalStroke;
    ctx.save=originalSave;
  }
  return segments;
}
function loadLabirintMaze(){
  horizontalWalls=create2D(ROWS+1,COLS,false);
  verticalWalls=create2D(ROWS,COLS+1,false);
  for(const {x1,y1,x2,y2} of captureLabirintSegments()){
    if(y1===y2){
      const row=Math.round((y1-PAD)/CELL);
      const startCol=Math.round((Math.min(x1,x2)-PAD)/CELL);
      const endCol=Math.round((Math.max(x1,x2)-PAD)/CELL);
      for(let c=startCol;c<endCol;c++) horizontalWalls[row][c]=true;
      continue;
    }
    if(x1===x2){
      const col=Math.round((x1-PAD)/CELL);
      const startRow=Math.round((Math.min(y1,y2)-PAD)/CELL);
      const endRow=Math.round((Math.max(y1,y2)-PAD)/CELL);
      for(let r=startRow;r<endRow;r++) verticalWalls[r][col]=true;
    }
  }
}
// Preveri stik igralca z enim zidom
function rectIntersectsWall(rx,ry,rw,rh,wx1,wy1,wx2,wy2){
  const minX=Math.min(wx1,wx2)-WALL_THICKNESS/2;
  const maxX=Math.max(wx1,wx2)+WALL_THICKNESS/2;
  const minY=Math.min(wy1,wy2)-WALL_THICKNESS/2;
  const maxY=Math.max(wy1,wy2)+WALL_THICKNESS/2;
  return rx<maxX&&rx+rw>minX&&ry<maxY&&ry+rh>minY;
}
// Preveri ali se igralec lahko premakne
function canMoveTo(newX,newY){
  const rx=newX,ry=newY,rw=PLAYER_SIZE,rh=PLAYER_SIZE;
  if(rx<PAD||ry<PAD||rx+rw>PAD+COLS*CELL||ry+rh>PAD+ROWS*CELL) return false;
  const startCol=Math.floor((rx-PAD)/CELL);
  const endCol=Math.floor((rx+rw-PAD)/CELL);
  const startRow=Math.floor((ry-PAD)/CELL);
  const endRow=Math.floor((ry+rh-PAD)/CELL);
  for(let r=Math.max(0,startRow);r<=Math.min(ROWS-1,endRow);r++){
    for(let c=Math.max(0,startCol);c<=Math.min(COLS-1,endCol);c++){
      const x=PAD+c*CELL,y=PAD+r*CELL;
      if(horizontalWalls[r][c]&&rectIntersectsWall(rx,ry,rw,rh,x,y,x+CELL,y)) return false;
      if(horizontalWalls[r+1][c]&&rectIntersectsWall(rx,ry,rw,rh,x,y+CELL,x+CELL,y+CELL)) return false;
      if(verticalWalls[r][c]&&rectIntersectsWall(rx,ry,rw,rh,x,y,x,y+CELL)) return false;
      if(verticalWalls[r][c+1]&&rectIntersectsWall(rx,ry,rw,rh,x+CELL,y,x+CELL,y+CELL)) return false;
    }
  }
  return true;
}
// Preveri zmago
function checkWin(){
  const goalX=END_POINT[0]-PLAYER_SIZE/2;
  const goalY=END_POINT[1]-PLAYER_SIZE;
  const overlap=player.x<goalX+PLAYER_SIZE&&player.x+PLAYER_SIZE>goalX&&player.y<goalY+PLAYER_SIZE&&player.y+PLAYER_SIZE>goalY;
  if(overlap&&!hasWon){
    hasWon=true;
    player=createPlayerStart();
    Swal.fire({title:"Končal si labirint!",text:"Vrnil si se na začetek.",icon:"success",confirmButtonText:"Super!"}).then(()=>{
      hasWon=false;
    });
  }
}
// Premikanje igralca
function updatePlayer(){
  if(hasWon) return;
  let dx=0,dy=0;
  if(keys.ArrowUp||keys.w) dy-=PLAYER_SPEED;
  if(keys.ArrowDown||keys.s) dy+=PLAYER_SPEED;
  if(keys.ArrowLeft||keys.a) dx-=PLAYER_SPEED;
  if(keys.ArrowRight||keys.d) dx+=PLAYER_SPEED;
  if(dx!==0&&dy!==0) dy=0;
  if(dx!==0&&canMoveTo(player.x+dx,player.y)) player.x+=dx;
  if(dy!==0&&canMoveTo(player.x,player.y+dy)) player.y+=dy;
  checkWin();
}
